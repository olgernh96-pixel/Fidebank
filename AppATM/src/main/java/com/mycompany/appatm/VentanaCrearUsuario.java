    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        //solicita nombre del cliente
        Banco banco = AppATM.banco;
        String nombre = nom.getText();
        if (nombre == null) return;
        String pin = Jpin.getText();
        //se utiliza pin.matches para verificar que el pin cumpla con las condiciones si no pasar al error
        if (pin == null || !pin.matches("\\d{4}")) {
            String error=("El PIN debe tener 4 d√≠gitos.");
            JOptionPane.showMessageDialog(null, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //solicita monto inicial
        double saldo = Double.parseDouble(Jmonto.getText());
        if (saldo < 0) return;
        //creamos un cliente con los datos de la cuenta
        Cliente cli = banco.abrirCuentaCliente(nombre, pin, saldo);
        if (cli == null) {
            String error1=("No se pudo abrir la cuenta.");
            JOptionPane.showMessageDialog(null, error1, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        GuardarArchivo.Guardar(cli);
        this.dispose();
        String info=("Cuenta creada:\nCliente: " + cli.getNombre() + "\nCuenta: " + cli.getNumeroCuenta());
        JOptionPane.showMessageDialog(null, info, "FideBank", JOptionPane.INFORMATION_MESSAGE);
    
    }//GEN-LAST:event_jButton1ActionPerformed

